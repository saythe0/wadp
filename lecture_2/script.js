// Задача 1: Запросите у пользователя число. Если число меньше 0, выведите сообщение "Число отрицательное". Если число больше 0, выведите сообщение "Число положительное". Если число равно 0, выведите сообщение "Число равно 0".
function checkNumber() {
	let number = parseFloat(prompt("Введите число:"));

	if (isNaN(number)) {
		alert("Ошибка: Введено не число");
	} else if (number < 0) {
		alert("Число отрицательное");
	} else if (number > 0) {
		alert("Число положительное");
	} else {
		alert("Число равно 0");
	}
}

// Задача 2: Запросите у пользователя два числа. Если оба числа меньше 10, выведите сообщение "Оба числа меньше 10". Если только одно число меньше 10, выведите сообщение "Только одно число меньше 10". Если ни одно число не меньше 10, выведите сообщение "Ни одно число не меньше 10".
function compareNumbers() {
	let num1 = parseFloat(prompt("Введите первое число:"));
	let num2 = parseFloat(prompt("Введите второе число:"));

	if (isNaN(num1) || isNaN(num2)) {
		alert("Ошибка: Одно или оба значения не являются числами");
	} else if (num1 < 10 && num2 < 10) {
		alert("Оба числа меньше 10");
	} else if (num1 < 10 || num2 < 10) {
		alert("Только одно число меньше 10");
	} else {
		alert("Ни одно число не меньше 10");
	}
}

// Задача 3: Запросите у пользователя строку. Если строка содержит только буквы, выведите сообщение "Строка состоит только из букв". В остальных случаях выведите сообщение "Строка содержит не только буквы".
function checkString() {
	let str = prompt("Введите строку:");
	let regex = /^[a-zA-Zа-яА-Я]+$/;

	if (regex.test(str)) {
		alert("Строка состоит только из букв");
	} else {
		alert("Строка содержит не только буквы");
	}
}